    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String name = txtName.getText();
        String age = txtAge.getText();
        String gender = txtGender.getText();
        String birthday = txtBirthday.getText();
        String phone = txtPhone.getText();
        String address = txtAddress.getText();
        
        userAccount.getEmployee().setName(name);
        userAccount.getEmployee().setAddress(address);
        userAccount.getEmployee().setAge(Integer.parseInt(age));
        userAccount.getEmployee().setBirthday(birthday);
        userAccount.getEmployee().setGender(gender);
        userAccount.getEmployee().setPhone(phone);
        
        btnSave.setEnabled(false);
        btnUpdate.setEnabled(true);
        
        txtAddress.setEnabled(false);
        txtAge.setEnabled(false);
        txtBirthday.setEnabled(false);
        txtGender.setEnabled(false);
        txtName.setEnabled(false);
        txtPhone.setEnabled(false);
    }//GEN-LAST:event_btnSaveActionPerformed

