    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed
        
        Enterprise enterprise = (Enterprise) enterpriseJComboBox.getSelectedItem();
        
        String username = usernameJTextField.getText();
        String password = String.valueOf(passwordJPasswordField.getPassword());
        String name = txtName.getText();
        
        if(username.equals("") || password.equals("") || name.equals("")){
            JOptionPane.showMessageDialog(null, "All fields should be required!","Warning",JOptionPane.WARNING_MESSAGE);
        }
        else if(!enterprise.getUserAccountDirectory().checkIfUsernameIsUnique(username)){
            JOptionPane.showMessageDialog(null, "Username has been existed!","Warning",JOptionPane.WARNING_MESSAGE);
        }
        else{
            Employee employee = enterprise.getEmployeeDirectory().createEmployee(name); 
            if(enterprise.getEnterpriseType()==Enterprise.EnterpriseType.Population){
                UserAccount account = enterprise.getUserAccountDirectory().createUserAccount(username, password, employee, new CustomerAdminRole());
            }
            if(enterprise.getEnterpriseType()==Enterprise.EnterpriseType.Gym){
                UserAccount account = enterprise.getUserAccountDirectory().createUserAccount(username, password, employee, new GymAdminRole());
            }
            if(enterprise.getEnterpriseType()==Enterprise.EnterpriseType.Restaurant){
                UserAccount account = enterprise.getUserAccountDirectory().createUserAccount(username, password, employee, new RestaurantAdminRole());
            }
            if(enterprise.getEnterpriseType()==Enterprise.EnterpriseType.PhysicalCenter){
                UserAccount account = enterprise.getUserAccountDirectory().createUserAccount(username, password, employee, new PhysicalAdminRole());
            }      
            JOptionPane.showMessageDialog(null, "Admin is created successfully!");
        }
        
        populateTable();
        
    }//GEN-LAST:event_submitJButtonActionPerformed

