    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRow = tabFitness.getSelectedRow();
        if(selectedRow >= 0){
            UserAccount userAccount = (UserAccount) tabFitness.getValueAt(selectedRow, 1);
            ViewFitnessJPanel viewFitnessJPanel = new ViewFitnessJPanel(userProcessContainer,userAccount);
            userProcessContainer.add("viewFitnessJPanel",viewFitnessJPanel);
            CardLayout layout=(CardLayout)userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
        else{
            JOptionPane.showMessageDialog(null, "Please select a row from table first","Warning",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnViewActionPerformed


