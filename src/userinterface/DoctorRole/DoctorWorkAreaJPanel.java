    private void btnReportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReportActionPerformed
        // TODO add your handling code here:
        String id = txtSearch.getText();
        UserAccount ua = null;
         for(Organization org : enterprise.getOrganizationDirectory().getOrganizationList()){
                ua = org.getUserAccountDirectory().findUserAccount(Integer.parseInt(id));
                if(ua != null) break;
            }
            if(ua != null){
                FitnessListJPanel fitnessListJPanel = new FitnessListJPanel(userProcessContainer, ua);
                userProcessContainer.add("fitnessListJPanel",fitnessListJPanel);
                CardLayout layout=(CardLayout)userProcessContainer.getLayout();
                layout.next(userProcessContainer);
            }
            else
                JOptionPane.showMessageDialog(null, "This id is not existing!","Warning",JOptionPane.WARNING_MESSAGE);
        
    }//GEN-LAST:event_btnReportActionPerformed

